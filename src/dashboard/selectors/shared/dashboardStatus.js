export const getNormalizedStatusToken = (value) => normalizeStatusToken(value);

export const normalizeStatusLabel = (value, options = {}) => {
    const { fallback = "Desconhecido" } = options;
    const token = normalizeStatusToken(value);

    if (!token) return fallback;

    return STATUS_LABELS[token] || fallback;
};

export const slugifyStatus = (value) => {
    const token = normalizeStatusToken(value);

    if (!token) return "";

    return STATUS_SLUG_ALIASES[token] || token;
};
